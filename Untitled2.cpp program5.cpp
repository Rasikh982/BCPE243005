#include <iostream>
using namespace std;
int main()
{
	int number;
	cout << "enter a number (1 to 7)"<<endl;
	cin >> number;
	if (number  == 1 ) {
		cout << "sunday" << endl;
	}
	else if (number == 2) {
		cout << "monday" << endl;
	}
	else if (number == 3) {
		cout << "tuesday";
	}
	else if (number == 4) {
		cout << "wednesday" << endl;
	}
	else if (number == 5) {
		cout << "thursday" << endl;
	}
	else if (number == 6) {
		cout << "friday" << endl;
	}
	else  {
		cout << "saturday" << endl;
	}
	



}





